				<br />

				<p class="margin">In 2004, a group of students had a vision of something greater than hanging up their suit and goggles after high school. This group consisted of Erin Selke, Jason Adour, and was lead by <a href="../Team/Alumni/Salzillo.php">Mark Salzillo</a>. Together, they worked to establish and bolster both a men's and women's competitive collegiate swim club at Northeastern. This was no small feat, as Northeastern requires that any new club sport is part of a national governing body. In order to create the club, <a href="../Team/Alumni/Salzillo.php">Mark Salzillo</a> and NUSC became one of the four founding members of the ASA U. Governing Body of Collegiate Club Swimming and the ASA National Championships.<br />
					<br />


					While the team was in its infant years, things moved slowly. It wasn't until 2005-2006, the second season, that the team even found a coach. With the help of Coach Dave Evangelista, the founders developed and grew the club. As they passed their knowledge and experience on, the team leaders were able to continue what they had started; in 2004, the team had less than 15 swimmers, and it consists of over 70 swimmers today.<br />
					<br />


					Each April for the first 6 years that the team existed, NUSC made 5 trips to various locations around the country to compete in the ASA Nationals. Some of these venues included Emory University, University of North Carolina, and Ohio State University. The best showing at this meet for Northeastern was in 2007, when both the women and the combined team finished in third place in a field of nearly 25 collegiate club teams. Northeastern is the only university to have attended every National Championship in the History of the ASA.<br />
					<br />


					Following the 2008-2009 season, NUSC made the decision to stop attending the ASA Championship meet and instead enter into a larger, more competitive championship. The ECC Nationals are held every year in Atlanta, Georgia at the Georgia Tech. Aquatic Center â€“ the site of the 1996 Olympics! The team's season has culminated here for every season since 2009. Through an influx of talented and dedicated swimmers, NUSC has been able to take itself to the next level of competition. The men's team has seen three consecutive top 10 finishes in a field of more than 60 other collegiate clubs at ECC Nationals, and the women's team cracked the top 5 for the first time in 2014. NUSC also combined placed 4th out of 67 teams that year.<br />
					<br />


					This success would not have been possible without the guidance and support of the club presidents throughout the years, as well as the coaches who have left their mark on the program. Below you can find a list of each, and the year they served.<br />
					<br />


				Looking forward, NUSC is ready to continue improving, and hopes to be ready to fight harder than ever to reach the top! Go Huskies!</p>
