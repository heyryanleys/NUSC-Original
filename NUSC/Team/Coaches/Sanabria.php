							<span style="margin">Ed joins the team with years of competitive experience as both a coach and an athlete.<br />
								<br />
								Originally from Southington, CT, Ed began his swimming career at the age of six for his local YMCA team. In high school, he was a 3 time All-Conference athlete, a CT State Finals and State Open meet qualifier, and a team captain during his serior year. He went on to swim Division III at Brandeis University as a freestyle and butterfly sprinter for three years. He later graduated from Central Connecticut State University with a degree in Psychology. Unfortunately, Ed was forced to to withdraw from the sport competitively due to a back injury, but he found a way to stay connected to his passion through coaching.<br />
								<br />
							Drawing from his own experiences and influences of former coaches, Ed is a technically driven coach, focusing on the details and fundamentals before distance and intensity. Having not taken advantage of the opportunities he had in high school has been a motivating factor behind his efforts. He looks to encourage his swimmers to be their best and tries to offer them the same opportunities that were offered to him in hopes that they will embrace them and have no regrets. Ed started coaching with age group swimmers and until he was able to take advantage of an opportunity to coach the boys and girls teams for his high school. So far in his young career he has coached a two time state champion, 3 All-State relays, 8 other swimmers and relays that have placed in the top 16 in CT, and multiple All-Conference athletes. Additionally, Ed has coached between 2-6 swimmers for each of the past three years at YMCA Nationals, one of the latest being 14 years old. Some of these swimmers are now part of programs at Georgetown, BC, Cornell, U. Chicago, Towson, and Clark. While he does not take the credit for their successes, he does value the experiences of being able to work with such talented swimmers and uses this to further push his swimmers and help them perform at their best.</span>
